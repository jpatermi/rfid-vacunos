     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Area  $area
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $area)
    {
        try {

            $data = $request->json()->all();
            $area = Area::find($area);
            if($area) {
                $area->name = $data['name'];
                $area->save();
                return response()->json($area, 201);
